Render::Render(const Camera& TheCamera, int nWidth, int nHeight) {
	_pCamera = &TheCamera;
	onSize(nWidth, nHeight);
}
	
void Render::onSize(int nWidth, int nHeight) {
	_nWidth		= nWidth;
	_nHeight	= nHeight;
}
	
void Render::update(double dElapsed) {
	BOOST_ASSERT(_pCamera);
	_MatProjection = glm::perspective(glm::radians(_pCamera->Zoom), (float)_nWidth / (float)_nHeight, 0.1f, 100.0f);
	_MatView = _pCamera->GetViewMatrix();
	_PosView = _pCamera->Position;
}

