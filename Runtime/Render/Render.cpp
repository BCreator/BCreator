/*standard geometry vbo for gizmos and so on*/
extern GLfloat g_BoxVertices[216];
static GLuint g_BoxVBO= 0;

int c2GetBoxVBOFloat() {
	if (g_BoxVBO)
		return g_BoxVBO;
	glGenBuffers(1, &g_BoxVBO);
	glBindBuffer(GL_ARRAY_BUFFER, g_BoxVBO);
	glBufferData(GL_ARRAY_BUFFER, sizeof(g_BoxVertices), g_BoxVertices, GL_STATIC_DRAW);
	return g_BoxVBO;
}

